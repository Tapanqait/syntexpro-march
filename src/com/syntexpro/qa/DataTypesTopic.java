package com.syntexpro.qa;

public class DataTypesTopic {

    public static void main(String[] args) {

        // byte

        byte a = 12;  //-128 to 127

        System.out.println(a);

        // short

        short b = 11236; // -32768 to 32767

        System.out.println(b);

        // int******

        int c = 659783;

        System.out.println(c);

        // long ****

        long d = 1234522l;

        System.out.println(d);

        //float**
        float e = 1.23F;
        System.out.println(e);

        //double*****
        double f = 1.23456789651289;
        System.out.println(f);

        // boolean*********
        boolean h = true;
        System.out.println(h);

        // char**

        char j = '$';
        System.out.println(j);

        //string data type - its non-primitive
        String x = " 123456787987643287765345988f";

        System.out.println(x);


    }
}
